export function useClientI18n() {
  const { locale, updateLocale } = useLocaleContext();
  const language: ClientLanguage = locale === "es" ? "es" : "en";

  const t = (key: TranslationKey) => clientTranslations[language][key];
  const text = (value: string) => textTranslations[language][value] ?? value;

  const setLanguage = (nextLanguage: ClientLanguage) =>
    updateLocale(nextLanguage as LocaleCode);

  return {
    language,
    setLanguage,
    t,
    text,
  };
}
